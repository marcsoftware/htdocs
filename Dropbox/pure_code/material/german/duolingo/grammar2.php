    <h3 class="p-2 mt-5 mb-2 bg-info text-white" id='26'>26 Lakes and seas - false friends ahoy!</h3>
    <div>
        <p>
            The German for "the lake" is <em>der See</em> (masculine) and the most commonly used word for "the sea" is
            <em>das Meer</em> (neuter).
        </p>
        <p>There's another slightly less commonly used word for "the sea": <em>die See</em> (feminine).</p>
        <p>
            Be careful not to confuse <em>der See</em> (the lake) and <em>die See</em> (the sea). Remember that when you
            learn a noun, you should always learn the gender with it.
        </p>
        <table class="table">
            <thead>
            <tr>
                <th><em>singular</em></th>
                <th>(masc.: "lake")</th>
                <th>(fem.: "sea")</th>
            </tr>
            </thead>
            <tbody>
            <tr>
                <td>nominative</td>
                <td><strong>der</strong> See</td>
                <td><strong>die</strong> See</td>
            </tr>
            <tr>
                <td>accusative</td>
                <td><strong>den</strong> See</td>
                <td><strong>die</strong> See</td>
            </tr>
            </tbody>
        </table>
        <p>The plural forms are identical (only the plural of <em>der See</em> is commonly used).</p>
        <table class="table">
            <thead>
            <tr>
                <th><em>plural</em></th>
                <th>(masc.: "lakes")</th>
                <th>(fem.: "seas")</th>
            </tr>
            </thead>
            <tbody>
            <tr>
                <td>nominative</td>
                <td>die Seen</td>
                <td>die Seen</td>
            </tr>
            <tr>
                <td>accusative</td>
                <td>die Seen</td>
                <td>die Seen</td>
            </tr>
            </tbody>
        </table>
        <p>There are not many noun pairs like this in German. Here is the most extreme example, with plural forms:</p>
        <ul>
            <li><em>das</em> Band (die Bänder) - the tape (band)</li>
            <li><em>der</em> Band (die Bände) - the volume/tome</li>
            <li><em>die</em> Band (pronounced as in English) (die Bands) - the music band</li>
        </ul>
