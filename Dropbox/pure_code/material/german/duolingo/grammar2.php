    <h4 class="p-2 mt-5 mb-2 bg-info text-white" id='24'>24 <em>Kleider</em> - dresses or clothes?</h4>
    <div>
        <p>
            <em>Das Kleid</em> means "the dress", and <em>die Kleider</em> means "the dresses", but the plural <em>
            die
            Kleider
        </em> can also mean "clothes" or "clothing". In most cases, "clothing" (or "clothes") translates to
            <em>Kleidung</em> (usually uncountable), but it's important to be aware that <em>Kleider</em> can be used in
            that sense as well.
        </p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white"  id='25'>25 <em>Hose</em> or <em>Hosen</em>?</h4>
    <div>
        <p>
            Both <em>Hose</em> and <em>Hosen</em> translate to "pants" ("trousers" in British English), but they're not
            interchangeable. The singular <em>Hose</em> refers to one pair of pants, and the plural <em>Hosen</em>
            refers to multiple pairs of pants.
        </p>
    </div>
