    <h3 class="p-2 mt-5 mb-2 bg-info text-white" id='1'>1 German plurals</h3>
    <div>
        <p>
            In English, making plurals out of singular nouns is typically as straightforward as adding an <em>-(e)s</em>
            at the end of the word:
        </p>
        <ul>
            <li>the dog, the dog<strong>s</strong></li>
        </ul>
        <p>In German, different nouns have different ways of forming the plural.</p>
        <p>
            Generally, you will probably have to memorize the plurals in the beginning. Later on, your brain will notice
            regular patterns that are not easily explained.
        </p>
        <p>
            However, there are some major regularities that are very helpful to know. If you apply these, the task of
            mastering German plurals will become much easier :)
        </p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white" id='2'>2 Ending in <em>-(e)n</em></h4>
    <div>
        <p>
            <strong>All nouns ending in <em>-e</em></strong>, and <strong>most feminine nouns</strong> will add an <em>-(e)n</em>
            ending in the plural.
        </p>
        <ul>
            <li>die Frau, die Frau<strong>en</strong></li>
            <li>die Ente, die Ente<strong>n</strong></li>
            <li>der Junge, die Junge<strong>n</strong></li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white"  id='3'>3 Ending in <em>-s</em></h4>
    <div>
        <p>Most nouns <strong>ending in a full vowel</strong> will add an <em>-s</em> in the plural.</p>
        <ul>
            <li>das Sof<strong>a</strong>, die Sofa<strong>s</strong></li>
            <li>das Aut<strong>o</strong>, die Auto<strong>s</strong></li>
            <li>das Bab<strong>y</strong>, die Baby<strong>s</strong></li>
            <li>das Caf<strong>é</strong>, die Café<strong>s</strong></li>
        </ul>
        <p>This does not apply to nouns ending in <em>-e</em> (which is not a full vowel).</p>
        <p>Many of these words are of foreign origin. Some other foreign words will also get the <em>-s</em> plural:</p>
        <ul>
            <li>der Chef (the boss), die Chef<strong>s</strong></li>
            <li>die Email, die Email<strong>s</strong></li>
            <li>der Job, die Job<strong>s</strong></li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white"  id='4'>4 No ending change</h4>
    <div>
        <p>
            There is <strong>no change</strong> for <strong>neuter or masculine</strong> nouns that have <strong>
            any of
            these singular endings
        </strong>:
        </p>
        <ul>
            <li>
                <p><strong>-chen, -lein, -el, or -er</strong>. </p>
            </li>
            <li>
                <p>das Mädchen, <strong>die</strong> Mädchen</p>
            </li>
            <li>der Computer, <strong>die</strong> Computer</li>
            <li>der Löffel (the spoon), <strong>die</strong> Löffel</li>
        </ul>
        <p>Some words for close family members will have an umlaut change:</p>
        <ul>
            <li>der Bruder (the brother), die Br<strong>ü</strong>der</li>
        </ul>
        <p>If words with these endings are <strong>feminine, the plural will end in <em>-n</em></strong>:</p>
        <ul>
            <li>die Schwester (the sister), die Schwester<strong>n</strong></li>
            <li>die Gabel (the fork), die Gabel<strong>n</strong></li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white"  id='5'>5 Ending in <em>-e/-er</em></h4>
    <div>
        <p>
            Most German <strong>one-syllable nouns</strong> will add an <em>-e</em> in their plural form. There might be
            an umlaut change.
        </p>
        <ul>
            <li>das Brot (the bread), die Brot<strong>e</strong></li>
            <li>der Tisch (the table), die Tisch<strong>e</strong></li>
            <li>der Ball (the ball), die B<strong>ä</strong>lle</li>
        </ul>
        <p>
            Many other <strong>masculine or neuter</strong> nouns will need the <em>-er</em> ending, and there may be
            umlaut changes.
        </p>
        <ul>
            <li>das Kind (the child), die Kind<strong>er</strong></li>
            <li>der Mann (the man), die M<strong>ä</strong>nn<strong>er</strong></li>
        </ul>
    </div>
    <h3 class="p-2 mt-5 mb-2 bg-info text-white"  id='6'>6 German feminine plurals - nouns ending in <em>-in</em></h3>
    <div>
        <p>Job descriptions are usually masculine:</p>
        <ul>
            <li>der Koch (the male cook)</li>
            <li>der Fahrer (the male driver)</li>
            <li>der Lehrer (the male teacher)</li>
            <li>der Arzt (the male physician)</li>
        </ul>
        <p>To refer to a female, German adds <em>-in</em>:</p>
        <ul>
            <li>die K<strong>ö</strong>ch<strong>in</strong> (the female cook)</li>
            <li>die Fahrer<strong>in</strong> (the female driver)</li>
            <li>die Lehrer<strong>in</strong> (the female teacher)</li>
            <li>die <strong>Ä</strong>rzt<strong>in</strong> (the female physician)</li>
        </ul>
        <p>As you can see, some of these get an umlaut change. The same umlaut change will happen in the plural.</p>
        <p>The plural of the masculine forms usually refers to mixed, as well as all-male groups:</p>
        <ul>
            <li>die K<strong>ö</strong>che (the cooks)</li>
            <li>die Fahrer (the drivers)</li>
            <li>die Lehrer (the teachers)</li>
            <li>die <strong>Ä</strong>rzte (the physicians)</li>
        </ul>
        <p>
            If you want to specify that you are talking about a group consisting of women, use the feminine plural forms.
            These will add <em>-innen</em> in the plural.
        </p>
        <ul>
            <li>die K<strong>ö</strong>ch<strong>innen</strong></li>
            <li>die Fahrer<strong>innen</strong></li>
            <li>die Lehrer<strong>innen</strong></li>
            <li>die <strong>Ä</strong>rzt<strong>innen</strong></li>
        </ul>
    </div>
</div>
<div class="row d-block">
    <h3 class="p-2 mt-5 mb-2 bg-info text-white"  id='7'>7 Predicate adjectives</h3>
    <div>
        <p>Predicate adjectives, i.e. <strong>adjectives that don't precede a noun, are not inflected</strong>.</p>
        <ul>
            <li>Der Mann ist <em>groß</em>.</li>
            <li>Die Männer sind <em>groß</em>.</li>
            <li>Die Frau ist <em>groß</em>.</li>
            <li>Die Frauen sind <em>groß</em>.</li>
            <li>Das Haus ist <em>groß</em>.</li>
            <li>Die Häuser sind <em>groß</em>.</li>
        </ul>
        <p>As you can see, the adjective remains in the base form, regardless of number and gender.</p>
        <p>
            "D'uh", you say? Keep digging into the German skills tree, and you will soon find the deeper reality of
            German adjectives :)
        </p>
    </div>
</div>
<div class="row d-block">
    <h3 class="p-2 mt-5 mb-2 bg-info text-white" id='8'>8 German Negatives - <em>nicht</em></h3>
    <div>
        <p>
            There are different ways to negate expressions in German (much like in English you can use "no" in some
            cases, and "does not" in others). The German adverb <em>nicht</em> (not) is used very often, but sometimes
            you need to use <em>kein</em> (not a). <em>Kein</em> will be taught in a later lesson.
        </p>
        <p><strong>Use <em>nicht</em></strong> in the following situations:</p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white"  id='9'>9 <em>Nicht</em> + definite article</h4>
    <div>
        <p><em>Nicht</em> negates a <strong>noun that has a definite article</strong>:</p>
        <ul>
            <li>Das ist <strong>nicht</strong> <em>der Junge</em>. (That is not the boy.)</li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white" id='10'>10 <em>Nicht</em> + possessive pronoun</h4>
    <div>
        <p><em>Nicht</em> negates a <strong>noun that has a possessive pronoun</strong>:</p>
        <ul>
            <li>Das ist <strong>nicht</strong> <em>mein Glas</em>. (That is not my glass.)</li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white" id='11'>11<em>Nicht</em> negates a verb</h4>
    <div>
        <p>When negating a verb, use <em>nicht</em>.</p>
        <ul>
            <li>Ich <em>trinke</em> <strong>nicht</strong>. (I do not drink.)</li>
        </ul>
        <p><strong>Why does the <em>nicht</em> appear at the end here?</strong></p>
        <p>Refer to the section "Position of <em>nicht</em>" below to find the answer.</p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white"  id='12'>12<em>Nicht</em> negates an adverb</h4>
    <div>
        <p><em>Nicht</em> appears before an <strong>adverb or adverbial phrase</strong>:</p>
        <ul>
            <li>Ich tanze <strong>nicht</strong> <em>oft</em>. (I don't dance often.)</li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white" id='13'>13<em>Nicht</em> negates an adjective at the end of a sentence</h4>
    <div>
        <p>When an adjective is part of a verb, also use <em>nicht</em>.</p>
        <ul>
            <li>Du bist <strong>nicht</strong> <em>hungrig</em>. (You are not hungry.)</li>
        </ul>
        <p>The infinitive here is <em>hungrig sein</em> (to be hungry).</p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white" id='14'>14 Position of <em>Nicht</em></h4>
    <div>
        <p>
            Adverbs end up in different places in different languages. You cannot simply place the German adverb <em>nicht</em>
            where you would put "not" in English.
        </p>
        <p>The general rule is:</p>
        <p><strong><em>Nicht</em> appears before the item it negates.</strong></p>
        <ul>
            <li>Du bist <strong>nicht</strong> <em>hungrig</em>. (not <em>hungry</em>)</li>
            <li>Ich tanze <strong>nicht</strong> <em>oft</em>. (not <em>often</em>)</li>
            <li>Das ist <strong>nicht</strong> <em>mein Glas</em>. (not <em>my glass</em>)</li>
            <li>Das ist <strong>nicht</strong> <em>der Junge</em>. (not <em>the boy</em>)</li>
        </ul>
        <p>So, what about <em>Ich trinke nicht</em>?</p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white"  id='15'>15 ♫ The German Sentence Bracket ♫</h4>
    <div>
        <p>Consider this English sentence:</p>
        <ul>
            <li>I <em>wake up</em> in China.</li>
        </ul>
        <p>
            The verb would be "wake up", the infinitive "to wake up". English keeps its verb elements close together.
            German, on the other hand, has a peculiar sentence structure:
        </p>
        <ul>
            <li>Ich <em>wache</em> in China <em>auf</em>.</li>
        </ul>
        <p>
            The infinitive here is <em>auf|wachen</em>. German will normally put the last element of the infinitive (the
            part that changes with the person) in position 2 of the sentence. Everything else will end up at the very
            end. The rest of the sentence (for example, adverbs), will appear between this "sentence bracket".
        </p>
        <p>Here's a longer example:</p>
        <ul>
            <li>
                <p>
                    Infinitive: <em>mit Freunden ins Restaurant <strong>gehen</strong></em> (to <em>go</em> to the
                    restaurant with friends)
                </p>
            </li>
            <li>
                <p>Ich <em>gehe</em> mit Freunden ins Restaurant.</p>
            </li>
        </ul>
        <p>
            If you're confused now, don't worry :) This will become clearer as you get lots of practice throughout this
            course.
        </p>
        <p>
            Why are we telling you this here? This bracket is the reason <em>nicht</em> might end up at the end of a
            sentence.
        </p>
        <p>Consider these examples:</p>
        <ul>
            <li>Ich lerne Deutsch. (I learn German.) — <em>Deutsch lernen</em> (to <em>learn German</em>)</li>
            <li>Ich trinke Bier. (I drink beer.) — <em>Bier trinken</em> (to <em>drink beer</em>)</li>
            <li>Ich trinke nicht. — <em>nicht trinken</em> ("to <em>not drink</em>")</li>
        </ul>
        <p><strong>This skill contains both negative and positive statements.</strong></p>
    </div>
</div>
<div class="row d-block">
    <h3 class="p-2 mt-5 mb-2 bg-info text-white" id='16'>16 Yes/No Questions</h3>
    <div>
        <p>When <strong>asking a yes/no question</strong> in English, you would say:</p>
        <ul>
            <li>"<strong>Is</strong> it cold?", but</li>
            <li>"<strong>Do</strong> you <strong>have</strong> a dog?" or</li>
            <li>"<strong>Does</strong> the man <strong>drink</strong> water?".</li>
        </ul>
        <p>German will not use "do" here. We will <strong>switch subject and verb for all verbs</strong>. </p>
        <ul>
            <li><strong>Ist</strong> es kalt?</li>
            <li><strong>Hast</strong> du einen Hund?</li>
            <li><strong>Trinkt</strong> der Mann Wasser?</li>
        </ul>
        <p><em>This skill contains both questions and statements.</em></p>
    </div>
</div>
<div class="row d-block">
    <h3 class="p-2 mt-5 mb-2 bg-info text-white" id='17'>17 No continuous aspect</h3>
    <div>
        <p>
            Remember that in German, there's no continuous aspect, i.e. there are no separate forms for "I drink" and "I
            am drinking". There's only one form: <em>Ich trinke.</em>
        </p>
        <p>There's no such thing as <em>Ich bin trinke</em> or <em>Ich bin trinken!</em></p>
    </div>
