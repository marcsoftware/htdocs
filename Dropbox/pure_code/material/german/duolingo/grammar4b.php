    <h4 class="p-2 mt-5 mb-2 bg-info text-white">20 Dative verbs</h4>
    <div>
        <p>
            Remember that some verbs have a dative object. This is just a quirk of German. There was a reason for it when
            these words were created, but it's not easy to understand anymore, after a lot of language change.
        </p>
        <p>In short, you just have to learn these :) There aren't very many.</p>
        <p>
            <em>Gehören</em> literally means to "belong to". But don't translate too literally, often a different
            translation will be more natural.
        </p>
        <ul>
            <li>Wem gehört das Kleid? ("Whose dress is it?" - Literally, "Whom does the dress belong to?")</li>
        </ul>
    </div>
