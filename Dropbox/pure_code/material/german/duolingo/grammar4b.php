    <h3 class="p-2 mt-5 mb-2 bg-info text-white"> 51 Imperative</h3>
    <div>
        <p>The imperative mood is used to express commands, just like in English.</p>
        <p>There are three different forms, according to the three types of "you" in German.</p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">52 <em>Du</em> imperative</h4>
    <div>
        <p>The imperative for <em>du</em> is very similar to English:</p>
        <ul>
            <li><em>Du gehst</em> nach Hause. (You go home.)</li>
            <li><em>Geh</em> nach Hause! (Go home!)</li>
        </ul>
        <p>For most verbs, to come up with the correct verb form, just lose the <em>-st</em> ending:</p>
        <ul>
            <li>Du arbeite<strong>st</strong> nachts. (You work at night)</li>
            <li>
                <p>Arbeite nachts! (Work at night!)</p>
            </li>
            <li>
                <p>Du nimm<strong>st</strong> das Taxi. (You take the taxi.)</p>
            </li>
            <li>Nimm das Taxi! (Take the taxi!)</li>
        </ul>
        <p>You might have noticed that some common verbs have an extra umlaut in the 2nd/3rd person singular:</p>
        <ul>
            <li>f<strong>a</strong>hren, du f<strong>ä</strong>hrst</li>
            <li>schl<strong>a</strong>fen, du schl<strong>ä</strong>fst</li>
        </ul>
        <p>In the imperative, <strong>these do not have an umlaut</strong>:</p>
        <ul>
            <li>Du f<strong>ä</strong>hrst mit dem Taxi.</li>
            <li>F<strong>a</strong>hr mit dem Taxi!</li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">53 <em>Ihr</em> imperative</h4>
    <div>
        <p>
            The second one is used to address more than one person informally. It uses the same conjugation as the
            regular <em>ihr</em> form of the present tense. This form of the imperative does not include a personal
            pronoun.
        </p>
        <ul>
            <li><em>Ihr fahrt</em> nach Paris. (You go to Paris.)</li>
            <li><em>Fahrt</em> nach Paris! (Go to Paris!)</li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">54 <em>Sie</em> imperative</h4>
    <div>
        <p>
            The third one is used to address one or more people formally. It uses the same conjugation as the regular
            <em>Sie</em> form of the present tense. The formal imperative is the only form to include the personal
            pronoun (<em>Sie</em>). Note that the word order is reversed. The verb always precedes the pronoun. It
            essentially looks like a question.
        </p>
        <ul>
            <li><em>Sie lernen</em> Deutsch. (You learn German.)</li>
            <li><em>Lernen Sie</em> Deutsch! (Learn German!)</li>
            <li>Lernen Sie Deutsch<strong>?</strong> (Do you learn German?)</li>
        </ul>
    </div>
