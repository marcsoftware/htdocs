    <h4 class="p-2 mt-5 mb-2 bg-info text-white">18 <em>Euro</em> or <em>Euros</em>?</h4>
    <div>
        <p>
            In German, the singular is <em>Euro</em> and the <strong>plural is usually <em>Euro</em></strong> as well. As
            a rule of thumb, use <em>Euro</em> when talking about a specific amount, e.g. <em>200 Euro</em>.
        </p>
        <p>
            In some contexts, the form <em>Euros</em> is used as well. For instance, you can say <em>Euros</em> to refer
            to individual euro coins, an unquantified amount of euros, or euros as opposed to a different currency,
            e.g.:
        </p>
        <ul>
            <li>
                Ich habe hundert Schweizer Franken, aber keine <em>Euros</em> (I have a hundred Swiss francs but no
                euros).
            </li>
        </ul>
        <p>Many native speakers use either plural form regardless of context.</p>
        <p>
            In English, either plural form is perfectly fine. The plural form <em>euro</em> tends to be preferred in the
            Republic of Ireland, and the plural form <em>euros</em> tends to preferred pretty much anywhere else.
            Originally, the plural form <em>euro</em> was supposed to be used in official EU documents, but that's no
            longer the case.
        </p>
    </div>
</div>
