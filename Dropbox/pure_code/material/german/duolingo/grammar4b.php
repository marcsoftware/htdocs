    <h3 class="p-2 mt-5 mb-2 bg-info text-white">26 <em>Seit</em></h3>
    <div>
        <p><em>Seit</em> roughly means "since". However, it works a bit differently.</p>
        <p>First, it always denotes something that is <strong>still going on</strong>.</p>
        <p>Second, it has <strong>three different ways</strong> of usage.</p>
        <p>Consider these examples:</p>
        <ul>
            <li>Ich lerne <em>seit sechs Jahren</em> Englisch. (I'm learning English for six years now.)</li>
            <li>Ich lerne <em>seit 2012</em> Englisch (I've been learning English since 2012.)</li>
            <li>Ich lerne Englisch, <em>seit ich denken kann</em>. (I've been learning English since I can think.)</li>
        </ul>
        <p>In the first example, <em>seit</em> defines a stretch of time, which reaches into the present.</p>
        <p>
            In the second example, it also defines a stretch of time, reaching into the present. But it defines this
            stretch of time by its starting point.
        </p>
        <p>
            <em>Seit</em> can also be a subordinating conjunction (check the lesson "Conjunctions"). In these, the verb
            leaves the second position of the sentence, and ends up at the end. This is why in the last example, <em>
            ich
            kann denken
        </em> (I can think) turns into <em>seit ich denken <strong>kann</strong></em>.
        </p>
    </div>
    <h3 class="p-2 mt-5 mb-2 bg-info text-white">27 <em>Zu Hause</em> vs. <em>nach Hause</em></h3>
    <div>
        <p>
            <em>Zu Hause</em> means <em>at home</em>, and <em>nach Hause</em> means <em>home</em> (<em>homewards</em>,
            not <em>at home</em>).
        </p>
        <p>
            The <em>-e</em> at the end of <em>zu Hause</em> and <em>nach Hause</em> is an archaic dative ending, which is
            no longer used in modern German, but survived in certain fixed expressions.
        </p>
        <ul>
            <li>
                <p>Ich bin zu Hause. (I am at home.)</p>
            </li>
            <li>
                <p>Ich gehe nach Hause. (I am walking home.)</p>
            </li>
        </ul>
    </div>
</div>
<div class="row d-block">
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">28 <em>Hals</em></h4>
    <div>
        <p>
            <em>Der Hals</em> refers to the whole connection between head and shoulders. German does have more
            specialized words for "neck" and "throat", but we normally use <em>Hals</em> for both.
        </p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">29<em>Haare</em></h4>
    <div>
        <p>
            <em>Das Haar</em> normally refers to a single hair. It <em>can</em> be used to refer to all the hair on
            someone's head, but is considered slightly outdated or poetic.
        </p>
        <ul>
            <li>Seine Haare sind lang. (ok)</li>
            <li>Sein Haar ist lang. (sounds a bit old)</li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">30<em>Bein</em></h4>
    <div>
        <p>
            <em>Das Bein</em> refers to the leg. It used to mean "bone" a long time ago. This meaning survives in some
            word combinations:
        </p>
        <ul>
            <li>Elfenbein (ivory, literally "elephant bone")</li>
            <li>Eisbein (pork knuckle, literally "ischias bone", because it referred to hip meat before)</li>
            <li>Beinhaus (bone house)</li>
            <li>Gebein(e) (a collection of bones)</li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">31<em>Magen</em></h4>
    <div>
        <p>
            <em>Der Magen</em> is the stomach, the part of your body that starts digestion. It is not commonly used to
            refer to the belly (<em>der Bauch</em>).
        </p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">32<em>Brust</em></h4>
    <div>
        <p><em>Die Brust</em> can have several meanings, depending on context.</p>
        <ul>
            <li>Komm an meine Brust! - This means the chest area. It will always be used in the singular.</li>
            <li>
                Vögel haben keine Brüste. (Birds don't have breasts) - This refers to female breasts. It can be used in
                the singular.
            </li>
        </ul>
    </div>
</div>
<div class="row d-block">
    <p>
        Surprise! There's another way of addressing people. The good news is: it's super easy. Just use the "they" forms
        when talking to people you're not close with.
    </p>
    <p>Need more details? Then read on :)</p>
</div>
<h3 class="p-2 mt-5 mb-2 bg-info text-white"> 33German You: Who are you talking to?</h3>
<div>
    <p>
        In English, "you" can be either singular or plural, and no distinction is made between formal and informal.
        In German, there are <strong>three ways of saying "you"</strong>.
    </p>
</div>
<h4 class="p-2 mt-5 mb-2 bg-info text-white">34<em>Du</em></h4>
<div>
    <p>
        If you are <strong><em>familiar</em> with someone, you use <em>du</em></strong> (which is called "duzen"). For
        example, if you talk to your mother, you would say:
    </p>
    <ul>
        <li>"Hast <em>du</em> jetzt Zeit, Mama?" (Do you have time now, Mommy?).</li>
    </ul>
    <p>Use this form for family members, co-students, children and young adults.</p>
</div>
<h4 class="p-2 mt-5 mb-2 bg-info text-white">35<em>Ihr</em></h4>
<div>
    <p>
        If you <strong>refer to more than one person, you use <em>ihr</em></strong>. This is also a "familiar" form, so
        use it in the same settings as <em>du</em>.
    </p>
    <p>The German <em>ihr</em> you learned earlier is the <strong>informal plural</strong> of "you," like in</p>
    <ul>
        <li>Hans und Karl, habt ihr Zeit? (Hans and Karl, do you have time?)</li>
    </ul>
</div>
<h4 class="p-2 mt-5 mb-2 bg-info text-white">36<em>Sie</em> (formal you)</h4>
<div>
    <p>
        If you are <strong>not familiar</strong> with someone or still wish to stay <strong>
        formal and express
        respect
    </strong>, you use <em>Sie</em> (so-called "siezen"). For example, you would always address your
        professor like this:
    </p>
    <ul>
        <li>Haben <em>Sie</em> jetzt Zeit, Herr Schmidt? (Do you have time now, Mr. Schmidt?)</li>
    </ul>
    <p>
        <em>Sie</em> is also used for multiple people. But you can't translate it well with "you all" or "you guys",
        because that would sound too informal.
    </p>
    <p>Here are the three forms of "you", and "they" for comparison:</p>
    <table class="table">
        <thead>
        <tr>
            <th>English person</th>
            <th>ending</th>
            <th>German example</th>
        </tr>
        </thead>
        <tbody>
        <tr>
            <td>you (singular informal)</td>
            <td>-st</td>
            <td>du trink<strong><em>st</em></strong></td>
        </tr>
        <tr>
            <td>you (plural informal)</td>
            <td>-t</td>
            <td>ihr trink<strong><em>t</em></strong></td>
        </tr>
        <tr>
            <td>you (formal)</td>
            <td>-en</td>
            <td>Sie trink<strong><em>en</em></strong></td>
        </tr>
        <tr>
            <td>they</td>
            <td>-en</td>
            <td>sie trink<strong><em>en</em></strong></td>
        </tr>
        </tbody>
    </table>
    <p>
        When spoken, <strong>"they" and formal "you" are identical</strong>. So, in a way, Germans formally address
        people like "How are they today?"
    </p>
</div>
<h4 class="p-2 mt-5 mb-2 bg-info text-white">37 How do you know if <em>sie</em> means "she", "they", or "you"?</h4>
<div>
    <p>
        You can distinguish the formal <em>Sie</em> from the plural <em>sie</em> (they) because the <strong>
        formal <em>Sie</em>
        will always be capitalized
    </strong>. However, it will remain ambiguous at the beginning of written sentences.
    </p>
    <p>
        For instance, <em>Sie sind schön.</em> can either refer to a beautiful individual or a group of beautiful people.
        The verbs for <em>sie</em> (they) and <em>Sie</em> (you) are conjugated the same. On Duolingo, either should be
        accepted unless the context suggests otherwise. In real life, there's always context. Don't worry about
        misunderstandings.
    </p>
    <p>
        Fortunately, the <strong>verb for <em>sie</em> (she) is different</strong>. <em>Sie ist schön.</em> only
        translates to "She is beautiful." There's no ambiguity.
    </p>
</div>
<h4 class="p-2 mt-5 mb-2 bg-info text-white">38 Other formal "you"s</h4>
<div>
    <p>
        There are more ways to address people formally in German, but they are not in common use and/or outdated, so we
        don't support them in this course. You might encounter them in Middle Ages reenactments or so :)
    </p>
    <p>The <em>third person singular</em> was used:</p>
    <ul>
        <li>Hat <em>er</em> heute gut geschlafen? (literally, "Has <em>he</em> slept well today?")</li>
    </ul>
    <p>The <em>second person plural</em> was also used, and is still used locally:</p>
    <ul>
        <li><em>Ihr</em> habt einen schönen Hut. (literally, "<em>You all</em> have a nice hat.")</li>
    </ul>
</div>
