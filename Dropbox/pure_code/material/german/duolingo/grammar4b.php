    <h4 class="p-2 mt-5 mb-2 bg-info text-white">21 Tall and short people</h4>
    <div>
        <p>Tall people are <em>groß</em>, not <em>hoch</em>, and short people are <em>klein</em>, not <em>kurz</em>.</p>
        <p>This is why German people will often refer to tall people as "big" :)</p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">22 <em>Cousin, Cousine</em></h4>
    <div>
        <p>
            These are French words. While it is possible to write <em>Cousine</em> as <em>Kusine</em> now, German never
            found a way to actually spell <em>Cousin</em> differently. This is because German originally does not have
            the French sound at the end. Some people pronounce it like "Kusäng" instead.
        </p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">
        23 <em>Die Frau kennt seinen Onkel</em> - Why not <em>ihren Onkel?</em>
    </h4>
    <div>
        <p>
            Both <em>Die Frau kennt ihren Onkel</em> and <em>Die Frau kennt seinen Onkel</em> are grammatically correct,
            but they don't have the same meaning.
        </p>
        <p>
            When you say <em>Die Frau kennt ihren Onkel</em>, you're either talking about the woman's own uncle, another
            female person's uncle, or the uncle of multiple people.
        </p>
        <p>
            When you say <em>Die Frau kennt seinen Onkel</em>, you're talking about another person's uncle, and that
            person is male. People can know other people's relatives.
        </p>
    </div>
</div>
