    <h3 class="p-2 mt-5 mb-2 bg-info text-white">61 Prepositions</h3>
    <div>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">62 Accusative prepositions</h4>
    <div>
        <p>Accusative prepositions <strong>always</strong> trigger the accusative case.</p>
        <p>Here are the most common ones: <strong>durch, für, gegen, ohne, um</strong></p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">63 Dative prepositions</h4>
    <div>
        <p>Dative prepositions <strong>always</strong> trigger the dative case.</p>
        <p>Here are the most common ones: <strong>aus, außer, bei, gegenüber, mit, nach, seit, von, zu</strong></p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">64 Two-way prepositions</h4>
    <div>
        <p>
            <strong>Two-way prepositions</strong> take the <strong>dative case or the accusative case</strong>, depending
            on the context.
        </p>
        <p>This is an unusual, but central part of German grammar.</p>
        <p>If there's <strong>movement from one place to another</strong>, use the <strong>accusative</strong> case.</p>
        <ul>
            <li>Die Katze geht <em>in die Küche</em>. (The cat walks into the kitchen.)</li>
        </ul>
        <p>
            If there's <strong>no movement</strong>, or if there's <strong>movement within a certain place</strong>, use
            the <strong>dative</strong> case.
        </p>
        <ul>
            <li>Die Katze schläft <em>in der Küche</em>. (The cat sleeps in the kitchen.)</li>
            <li>Die Katze geht <em>in der Küche</em>. (The cat walks within the kitchen.)</li>
        </ul>
        <p>
            These prepositions can switch case: <strong>an, auf, hinter, in, neben, über, unter, vor, zwischen</strong>
        </p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">65 When <em>not</em> to think about location change</h4>
    <div>
        <p>Two-way prepositions are very common in everyday speech, so it's a good idea to practice them to fluency.</p>
        <p>However, don't forget that <strong>for some prepositions, you don't have to decide</strong>:</p>
        <p>
            <em>Durch</em> and <em>um</em> will always be accusative, although they might signify an activity without
            location change:
        </p>
        <ul>
            <li>Das Kind rennt <em>durch den</em> Wald. (The child is running through the forest.)</li>
            <li>Die Stühle stehen <em>um den</em> Tisch. (The chairs are standing around the table.)</li>
        </ul>
        <p>
            <em>Aus</em>, <em>von</em>, <em>zu</em> will always be dative, although they might signify a location change.
        </p>
        <ul>
            <li>Er kommt <em>aus der</em> Küche (He comes out of the kitchen.)</li>
            <li>Ich fahre <em>zur</em> Arbeit. (I go to work.)</li>
            <li>Ich komme <em>von der</em> Arbeit. (I come from work.)</li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">66 Other uses for two-way prepositions</h4>
    <div>
        <p>
            Some verbs use one of these prepositions in a way that is <em>not about location</em>. This is part of
            language change, where things get repurposed all the time.
        </p>
        <p><em>Über</em> will always trigger the accusative case:</p>
        <ul>
            <li>Sie diskutieren <em>über den</em> Krieg. (They discuss the war.)</li>
        </ul>
        <p>When used with these verbs, <em>vor</em> will always trigger the Dative:</p>
        <ul>
            <li>Er warnt <em>vor dem</em> Hund. (He warns about the dog.)</li>
        </ul>
        <p>
            <em>An</em>, <em>in</em> and <em>auf</em> are more complicated: in some verbs, they trigger the accusative,
            in others the dative. You'll just have to memorize these.
        </p>
        <ul>
            <li>Er <em>denkt</em> <em>an seinen</em> Bruder. (He thinks of his brother.)</li>
            <li>
                <p>Er <em>arbeitet</em> <em>an einem</em> Film (He's working on a film.)</p>
            </li>
            <li>
                <p>Ich <em>warte</em> <em>auf den</em> Bus. (I'm waiting for the bus.)</p>
            </li>
            <li>Der Film <em>basiert</em> <em>auf meinem</em> Leben. (The film is based on my life.)</li>
        </ul>
    </div>
