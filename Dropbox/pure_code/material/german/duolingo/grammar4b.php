            <li>Ich schmecke Knoblauch! (I taste garlic!)</li>
            <li>Knoblauch schmeckt super! (Garlic tastes great!)</li>
        </ul>
        <p>In addition, <em>schmecken</em> can be used by itself:</p>
        <ul>
            <li>Die Pizza schmeckt nicht! (The pizza does not taste <em>good</em>!)</li>
