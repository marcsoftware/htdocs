                    In the <strong>third person</strong>, the <strong>endings are the same as for the articles</strong>:
                    <em>-m, -r, -n</em>. However, plural dative is "ihn<strong>en</strong>" (not <em>ihn</em>, as you
                    might expect).
                </p>
            </li>
            <li>
                <p>In the second person plural, accusative and dative pronouns are the same.</p>
            </li>
        </ul>
        <p>
            Now you can understand why, when thanking a female person, it is only correct to say <em>Ich danke ihr</em>
