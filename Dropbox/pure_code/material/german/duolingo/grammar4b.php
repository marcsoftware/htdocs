    <h3 class="p-2 mt-5 mb-2 bg-info text-white">58 Dropping articles</h3>
    <div>
        <p>
            When talking about your or someone else's profession in sentences such as <em>I'm a teacher</em> or <em>
            She's
            a judge
        </em>, German speakers <strong>usually drop the indefinite article</strong> <em>(ein/eine)</em>.
        </p>
        <ul>
            <li>Ich bin Lehrer. (I am <strong>a</strong> teacher.)</li>
        </ul>
        <p>
            It sounds more natural to say <em>Ich bin Lehrer</em> and <em>Sie ist Richterin</em> than <em>
            Ich bin ein
            Lehrer
        </em> and <em>Sie ist eine Richterin</em>. This rule also applies to students.
        </p>
        <p>
            If you <strong>add an adjective</strong>, you <strong>can't drop the article</strong>. <em>
            Er ist ein
            schlechter Arzt
        </em> (He's a bad doctor) is correct, but <em>Er ist schlechter Arzt</em> is not.
        </p>
        <p>Also note that <strong>you can't drop the definite article</strong> <em>(der/die/das)</em>.</p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white" >59 Male and female variants</h4>
