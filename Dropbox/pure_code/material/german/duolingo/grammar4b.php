    <h4 class="p-2 mt-5 mb-2 bg-info text-white"> 16 Plural Nouns in Dative</h4>
    <div>
        <p>Here's a great rule:</p>
        <p><strong>Plural Dative: Everything gets an <em>-n</em></strong></p>
        <p>(Insert Oprah Winfrey GIF here)</p>
        <p>You just saw that articles (also pronouns etc.) get an <em>-n</em> ending in dative plural.</p>
        <p>
            Later, you will learn that the German ending system for adjectives is a bit complicated. However, in dative
            plural, you just <strong>add an <em>-n</em></strong>.
        </p>
        <p>It goes so far that even <strong>plural forms of nouns get an extra <em>-n</em> in the Dative</strong>.</p>
        <ul>
            <li>Er hat <em>drei Hunde</em>. Er spielt <em>mit drei Hunden</em>. (He plays with three dogs.)</li>
            <li>Die Computer sind alt. Ich antworte <em>den Computern</em>. (I answer the computers.)</li>
        </ul>
        <p>There are two "exceptions":</p>
        <ul>
            <li>If the plural already end in <em>-n</em>, you're set.</li>
            <li>If the plural ends in <em>-s</em>, there's also no change.</li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">17 Even more <em>-n</em></h4>
    <div>
        <p>
            <strong>Some masculine nouns add an <em>-en</em> or <em>-n</em> ending</strong> in the dative and in <strong>
            all
            other cases besides the nominative
        </strong>. For example in the dative, it is <em>dem Jungen</em> (the boy).
        </p>
        <p>If you want to look these up, the term for them is "n-Declension".</p>
    </div>
