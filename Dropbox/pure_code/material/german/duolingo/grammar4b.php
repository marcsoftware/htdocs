    <h3 class="p-2 mt-5 mb-2 bg-info text-white">69 <em>Zu Hause</em> vs. <em>nach Hause</em></h3>
    <div>
        <p>
            <em>Zu Hause</em> means <em>at home</em>, and <em>nach Hause</em> means <em>home</em> (<em>homewards</em>,
            not <em>at home</em>). The <em>-e</em> at the end of <em>zu Hause</em> and <em>nach Hause</em> is an archaic
            dative ending, which is no longer used in modern German, but survives in certain fixed expressions.
        </p>
        <ul>
            <li>
                <p>Ich bin <em>zu Hause</em>. (I am <em>at home</em>.)</p>
            </li>
            <li>
                <p>Ich gehe <em>nach Hause</em>. (I am walking <em>home</em>.)</p>
            </li>
        </ul>
