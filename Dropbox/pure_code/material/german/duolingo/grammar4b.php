    <h3 class="p-2 mt-5 mb-2 bg-info text-white">11The Dative Case</h3>
    <div>
        <p>Welcome to the third important case in German :) Later on, there will be a last, less important one.</p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">12Remember the Accusative ?</h4>
    <div>
        <p>You already saw that the <strong>accusative case</strong> can be used in different ways.</p>
        <p>It can signify the <em>object of a sentence</em>:</p>
        <ul>
            <li>Der Hund frisst <em>den Vogel</em>. (The dog is eating the bird.)</li>
        </ul>
        <p>This is called the <strong>direct object</strong> (or <strong>accusative object</strong>).</p>
        <p>It can also be used <em>in combination with some prepositions</em>:</p>
        <ul>
            <li>Sie geht <em>ohne den Hund</em>. (She walks without the dog.)</li>
            <li>Er hat einen Mantel <em>ohne Knöpfe</em>. (He has a coat without buttons.)</li>
        </ul>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">13 Dative object</h4>
    <div>
        <p>The <strong>dative case</strong> also <strong>has a range of different functions</strong>.</p>
        <p>
            In this lesson, you learn to use it with the <strong>indirect object</strong>. This is also called the
            <strong>dative object</strong>.
        </p>
        <p>The indirect object in a sentence is the <strong>receiver of the direct (accusative) object</strong>.</p>
        <p>For example, <em>Frau</em> is the indirect (dative) object in</p>
        <ul>
            <li>Das Mädchen gibt <em>einer Frau</em> den Apfel. (A girl gives the apple <em>to a woman</em>.)</li>
        </ul>
        <p>You can think about it as "the other person involved" in a transaction.</p>
        <ul>
            <li>Ich gebe <em>dem Mann</em> einen Apfel. (I give <em>the man</em> an apple.)</li>
            <li>Sie zeigt <em>dem Kind</em> den Hund. (She shows <em>the child</em> the dog.)</li>
        </ul>
        <p>Generally, the <strong>dative object comes before the accusative object</strong>.</p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">14 Dative verbs</h4>
    <div>
        <p>
            The dative is also used for certain dative verbs such as <em>danken</em> (to thank) and <em>antworten</em>
            (to answer), or <em>helfen</em> (to help):
        </p>
        <ul>
            <li>Ich danke <em>dem Kind</em>. (I thank the child.)</li>
            <li>Ich helfe <em>der Frau</em>. (I help the woman.)</li>
            <li>Ich antworte <em>meinem Bruder</em>. (I answer my brother.)</li>
        </ul>
        <p>These verbs don't have an accusative object.</p>
    </div>
    <h4 class="p-2 mt-5 mb-2 bg-info text-white">15 Dative articles</h4>
